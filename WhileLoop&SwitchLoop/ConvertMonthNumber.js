
// Convert Month Number to Name: Write a program that converts a
// numerical month (1 for January, 2 for February, etc.) into the name of the
// month using a switch statement.

var month =  1;
switch(month) {
   
    case 1:
        console.log( month = 'January');
        break;
    case 2:
        month = "February";
        console.log( month = 'January')
        break;
    case 3:
        console.log( month = 'March');
        break;
    case 4:
        console.log( month = 'April');
        break;
    case 5:
        console.log( month = 'May');
        break;
    case 6:
        console.log( month = 'June');
        break;
    case 7:
        console.log( month = 'July');
        break;
    case 8:
        console.log( month = 'August');
        break;
    case 9:
        console.log( month = 'September');
        break;
    case 10:
        console.log( month = 'October');
        break;
    case 11:
        console.log( month ='November');
        break;
    case 12:
        console.log( month = 'December');
        break;
}
