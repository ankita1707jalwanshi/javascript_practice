
// Traffic Light Simulation: Develop a program that simulates a traffic light
// using a switch statement. It should take a color (e.g., "red," "yellow,"
// "green") as input and display the appropriate message based on the color.


var trafficLight = 'green'
switch(trafficLight){
    case "red":
        console.log("Red Light means you should stop here");
        break;
    case "yellow":
        console.log("Yellow Light means you should ready to go here");
        break;        
    case "green":
        console.log("Green Light means you should go from here");
        break;
}