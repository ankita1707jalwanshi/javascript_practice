
// Grade Calculator: Build a grade calculator program that accepts a
// numerical score (0 to 100) and uses a switch statement to determine the
// corresponding letter grade (A, B, C, D, or F).

var grade = "A";

switch(grade){
    case "A":
        console.log("Grade - A You are Topper");
        break;
    case "B":
         console.log("Grade - B You are Second Topper");
         break;
    case "C":
        console.log("Grade - C You are third Topper");
        break;
    case "D":
        console.log("Grade - D Good performance");
        break;
    case "E":
        console.log("Grade - E Average Performance");
        break;
    case "F":
        console.log("Grade - F You are Fail");
        break;
                         
            
}